package If_Statements_Programs;

public class Sample {
    public static void main(String[] args) {
        System.out.println();
        int x, y;
        x = 10;
        y = 20;
        if (x < y) {
            System.out.println("X is less than y");
        }
        if (x > y) {
            System.out.println("X is greater than y");
        }
        x = x * 2;
        if (x == y) {
            System.out.println("X is equal to y");
        }
    }
}
