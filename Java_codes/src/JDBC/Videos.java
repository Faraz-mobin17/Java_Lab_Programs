class Videos {
    private int id, user_id, duration, views;
    private String videoFile, thumbnail, title, description;
    private boolean isPublished;

    public Videos() {
    }

    public Videos(int id, int user_id, int duration, int views, String videoFile, String thumbnail, String title,
            String description, boolean isPublished) {
        this.id = id;
        this.user_id = user_id;
        this.duration = duration;
        this.views = views;
        this.videoFile = videoFile;
        this.thumbnail = thumbnail;
        this.title = title;
        this.description = description;
        this.isPublished = isPublished;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public int getViews() {
        return views;
    }

    public void setViews(int views) {
        this.views = views;
    }

    public String getVideoFile() {
        return videoFile;
    }

    public void setVideoFile(String videoFile) {
        this.videoFile = videoFile;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isPublished() {
        return isPublished;
    }

    public void setPublished(boolean isPublished) {
        this.isPublished = isPublished;
    }

}
